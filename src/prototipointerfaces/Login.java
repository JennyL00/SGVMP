    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        if (txtUsuario.getText().equals("")
                || txtPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Para ingresar al sistema debe llenar TODOS los campos", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            //----
            String usuario = txtUsuario.getText();
            //----
            String pass = "";
            char[] password = txtPassword.getPassword();
            for (int x = 0; x < password.length; x++) {
                pass += password[x];
            }
            //----
            if (String.valueOf(cmbTipo.getSelectedItem()).equals("Seleccionar...")) {
                JOptionPane.showMessageDialog(null, "¡Seleccione un Tipo de Usuario!");
            } else {
                String tipo = String.valueOf(cmbTipo.getSelectedItem());
                if (!estaRegistrado(usuario, pass, tipo)) {
                    JOptionPane.showMessageDialog(null, "Usuario o Password incorrectos - Intente nuevamente \n Número de intentos restantes: "+(flag-1), "Error", JOptionPane.WARNING_MESSAGE);
                    txtUsuario.setText("");
                    txtPassword.setText("");
                    cmbTipo.setSelectedIndex(0);
                    flag=flag-1;
                    if(flag<=0){
                        JOptionPane.showMessageDialog(null, "Numero de intentos finalizados", "Error", JOptionPane.WARNING_MESSAGE);
                        System.exit(0);
                    }
                } else {
                    switch (tipo) {
                        case "Gerente General":
                            GerenteGeneral nuevoGerente = new GerenteGeneral();
                            nuevoGerente.setVisible(true);
                            this.dispose();
                            break;

                        case "Vendedor de Mostrador":
                            VendedorDeMostrador nuevoVendedor = new VendedorDeMostrador();
                            nuevoVendedor.setVisible(true);
                            this.dispose();
                            break;

                        case "Encargado de Bodega":
                            MóduloProductos nuevoModProd = new MóduloProductos();
                            nuevoModProd.setVisible(true);
                            this.dispose();
                            break;

                        default:
                            break;
                    }
                }
            }
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

