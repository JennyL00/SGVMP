    boolean estaRegistrado(String usuario, String password, String tipoUsuario) {
        String sql = "SELECT * FROM usuarios WHERE usuario LIKE ? AND password LIKE ? AND tipo_usuario LIKE ?";
        String datos[] = new String[3];

        Conexion con = new Conexion();
        Connection cn = (Connection) con.conexion();
        PreparedStatement pst;
        ResultSet rs;

        try {
            pst = (PreparedStatement) cn.prepareStatement(sql);
            //usuario = "'" + usuario + "'";
            //password = "'" + password + "'";
            pst.setString(1, usuario);
            pst.setString(2, password);
            pst.setString(3, tipoUsuario);
            rs = pst.executeQuery();
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(4);
            }
            if (datos[0] == null || datos[1] == null || datos[2] == null) {
                //JOptionPane.showMessageDialog(null, "Cliente no registrado - Primero registre el Cliente");
                return false;
            }
        } catch (SQLException e) {
            System.err.println("Error: " + e);
        }
        return true;
    }

